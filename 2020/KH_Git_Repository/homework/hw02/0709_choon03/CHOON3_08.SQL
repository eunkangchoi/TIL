--문제8. 과목별 교수이름을 찾으려고한다. 과목이름과 교수이름을
-- 출력하는 SQL문을 작성해라.

-- 풀이1. 1575행이 나옴.
SELECT CLASS_NAME 과목이름, PROFESSOR_NAME 교수이름
FROM TB_PROFESSOR
    JOIN TB_CLASS USING (DEPARTMENT_NO);

SELECT CLASS_NAME 과목이름, PROFESSOR_NAME 교수이름
FROM TB_CLASS
    JOIN TB_PROFESSOR USING (DEPARTMENT_NO);


-- 풀이2. 정답
SELECT CLASS_NAME 과목이름, PROFESSOR_NAME 교수이름
FROM TB_CLASS
    JOIN TB_CLASS_PROFESSOR USING (CLASS_NO)-- TB_CLASS_PROFESSOR 과 내부조인
    JOIN TB_PROFESSOR USING(PROFESSOR_NO); -- TB_PROFESSOR과 내부조인
