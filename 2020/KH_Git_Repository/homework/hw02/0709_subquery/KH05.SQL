--문제5. 보너스를 받는 사원의 사원명, 부서명, 지역명 조회
-- 1) EMPLOYEE테이블과 DEPARTMENT 테이블과 내부조인
SELECT EMP_NAME 사원명, DEPT_TITLE 부서명, LOCATION_ID 지역코드
FROM EMPLOYEE
    JOIN DEPARTMENT ON (DEPT_CODE=DEPT_ID);

-- 2) 1)의 결과에서 LOCATION 테이블과 조인
SELECT EMP_NAME 사원명, DEPT_TITLE 부서명, LOCAL_NAME 지역명
FROM EMPLOYEE
    JOIN DEPARTMENT ON (DEPT_CODE=DEPT_ID)
    JOIN LOCATION ON (LOCATION_ID=LOCAL_CODE);

-- 3) 보너스를 받는 사원을 찾는다.
-- ANSWER
SELECT EMP_NAME 사원명, BONUS 보너스, DEPT_TITLE 부서명, LOCAL_NAME 지역명
FROM EMPLOYEE
    JOIN DEPARTMENT ON (DEPT_CODE=DEPT_ID)
    JOIN LOCATION ON (LOCATION_ID=LOCAL_CODE)
WHERE BONUS IS NOT NULL;
