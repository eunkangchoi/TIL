--문제7. TB_CATEGORY 테이블과 TB_CLASS_TYPE 테이블의 PRIMARY KEY 이름을
-- 다음과 같이 변경하시오.
-- PRIMARY KEY 이름은 "PK_+컬럼이름"으로 지정하시오.

--1) TB_CATEGORY 테이블의 PRIMARY KEY 이름 변경
-- TB_CATEGORY 테이블의 PRIMARY KEY : CATEGORY_NAME
--덤 COMMENT를 추가한다.
ALTER TABLE TB_CATEGORY
    RENAME COLUMN CATEGORY_NAME TO PK_CATEGORY_NAME;

COMMENT ON COLUMN TB_CATEGORY.PK_CATEGORY_NAME IS '이름(PK)';

--2) TB_CLASS_TYPE 테이블의 PRIMARY KEY 이름 변경
-- TB_CLASS_TYPE테이블의 PRIMARY KEY: NAME
ALTER TABLE TB_CLASS_TYPE
    RENAME COLUMN CLASS_TYPE_NAME TO PK_CLASS_TYPE_NAME;

COMMENT ON COLUMN TB_CLASS_TYPE.PK_CLASS_TYPE_NAME IS '이름(PK)';
