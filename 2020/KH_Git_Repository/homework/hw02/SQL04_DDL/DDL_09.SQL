
--문제9. TB_DEPARTMENT 의 CATEGORY컬럼이
-- TB_CATEGORY 테이블의 CATEGORY_NAME컬럼을
-- 부모값으로 참조하도록 FOREIGN KEY를 지정하시오.
-- 이때 FOREIGN KEY 이름은 FK_테이블이름_컬럼이름 으로 지정한다.

--1) TB_DEPARTMENT 테이블의 CATEGORY컬럼이
--   TB_CATEGORY 테이블의 CATEGORY_NAME컬럼을 부모값으로 참조하도록한다.
ALTER TABLE TB_DEPARTMENT
    ADD FOREIGN KEY (CATEGORY) REFERENCES TB_CATEGORY(PK_CATEGORY_NAME);

--2) 키 이름은 FK_DEPARTMENT_CATEGORY 로 변경한다.
ALTER TABLE TB_DEPARTMENT
    RENAME COLUMN CATEGORY TO FK_DEPARTMENT_CAEGORY;

--3) FOREIGN KEY에대한 제약조건이름을 변경한다.( SYS_C007239=>FK_DEPARTMENT_CATEOGRY)
ALTER TABLE TB_DEPARTMENT
    RENAME CONSTRAINT SYS_C007239 TO FK_DEPARTMENT_CATEGORY;
