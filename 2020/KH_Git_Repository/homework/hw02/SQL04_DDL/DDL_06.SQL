--문제6. 두 테이블의 NO컬럼과 NAME 컬럼의 이름을 각각 TB_를 제외한
-- 테이블 이름이 앞에 붙은 형태로 변경한다.

-- 컬럼이름 변경: ALTER TABLE [테이블명] RENAME COLUMN [컬럼이름] TO [바꿀컬럼이름]
-- 예 TB_CLASS_TYPE 테이블의 NAME컬럼 : CLASS_TYPE_NAME
-- 예 TB_CATEGORY 테이블의 NAME컬럼 : CATEGORY_NAME


--1) TB_CLASS_TYPE 테이블의 NO컬럼 => CLASS_TYPE_NO 로 컬럼이름 변경
ALTER TABLE TB_CLASS_TYPE
    RENAME COLUMN NO TO CLASS_TYPE_NO;

--2) TB_CLASS_TYPE 테이블의 NAME컬럼 => CLASS_TYPE_NAME 으로 컬럼이름 변경
ALTER TABLE TB_CLASS_TYPE
    RENAME COLUMN NAME TO CLASS_TYPE_NAME;

--3) TB_CATEGORY 테이블의 NAME컬럼 => CATEGORY_NAME으로 컬럼이름변경
ALTER TABLE TB_CATEGORY
    RENAME COLUMN NAME TO CATEGORY_NAME;
