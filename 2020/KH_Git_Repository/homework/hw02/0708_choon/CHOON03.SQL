-- 문제3
-- 국어국문학과에 다니는 여학생 중 현재 휴학중인 여학생
-- 단, 국문학과의 '학과코드'는 학과테이블(TB_DEPARTMENT)을 조회해서 찾아내도록한다.

-- 국어국문학과 학과코드 조회 => 001
SELECT DEPARTMENT_NO, DEPARTMENT_NAME
FROM TB_DEPARTMENT;

-- 학과코드(DEPARTMENT_NO)의 타입은 VARCHAR2(문자열)
SELECT TB_STUDENT.STUDENT_NAME AS STUDENT_NAME
FROM TB_STUDENT, TB_DEPARTMENT
WHERE TB_STUDENT.DEPARTMENT_NO= TB_DEPARTMENT.DEPARTMENT_NO -- TB_DEPARTMENT와 TB_STUDENT INNER JOIN
    AND TB_DEPARTMENT.DEPARTMENT_NO='001' -- 국어국문학과
    AND TO_NUMBER(SUBSTR(TB_STUDENT.STUDENT_SSN, 8,1))=2 -- 여학생(주민번호의 8번째자리가 2)
    AND TB_STUDENT.ABSENCE_YN='Y'; -- 휴학상태 (ABSENCE_YN=Y)




-- 다른 풀이
SELECT TB_STUDENT.STUDENT_NAME AS STUDENT_NAME
FROM TB_STUDENT, TB_DEPARTMENT
WHERE TB_STUDENT.DEPARTMENT_NO= TB_DEPARTMENT.DEPARTMENT_NO -- TB_DEPARTMENT와 TB_STUDENT INNER JOIN
    AND TB_DEPARTMENT.DEPARTMENT_NAME='국어국문학과' -- 국어국문학과
    AND TO_NUMBER(SUBSTR(TB_STUDENT.STUDENT_SSN, 8,1))=2 -- 여학생(주민번호의 8번째자리가 2)
    AND TB_STUDENT.ABSENCE_YN='Y'; -- 휴학상태 (ABSENCE_YN=Y)
